<template>
  <v-card>
    <v-card-title class="text-center">
      <span class="text-h5 text-uppercase">{{ t('event.form.editTitle') }}</span>
    </v-card-title>
    <v-card-text>
      <EventForm
        ref="eventFormRef"
        v-if="event"
        :initial-event-data="event"
        :read-only="false"
      />
      <v-progress-circular
        v-else
        indeterminate
        color="primary"
      ></v-progress-circular>
    </v-card-text>
    <v-card-actions>
      <v-spacer></v-spacer>
      <v-btn color="blue-darken-1" variant="text" @click="closeForm">{{ t('common.cancel') }}</v-btn>
      <v-btn color="blue-darken-1" variant="text" @click="handleUpdateEvent">{{ t('common.save') }}</v-btn>
    </v-card-actions>
  </v-card>
</template>

<script setup lang="ts">
import { ref, onMounted } from 'vue';
import { useI18n } from 'vue-i18n';
import { useRouter, useRoute } from 'vue-router';
import { useEventStore } from '@/stores/event.store';
import { useNotificationStore } from '@/stores/notification.store';
import EventForm from '@/components/events/EventForm.vue';
import type { Event } from '@/types/event/event';

interface EventFormExposed {
  validate: () => Promise<boolean>;
  getFormData: () => Event | Omit<Event, 'id'>;
}

const eventFormRef = ref<EventFormExposed | null>(null);

const { t } = useI18n();
const router = useRouter();
const route = useRoute();
const eventStore = useEventStore();
const notificationStore = useNotificationStore();

const event = ref<Event | undefined>(undefined);

onMounted(async () => {
  const eventId = route.params.id as string;
  if (eventId) {
    event.value = await eventStore.fetchItemById(eventId);
  }
});

const handleUpdateEvent = async () => {
  if (!eventFormRef.value) return;
  const isValid = await eventFormRef.value.validate();
  if (!isValid) return;

  const eventData = eventFormRef.value.getFormData() as Event;
  if (!eventData.id) {
    notificationStore.showSnackbar(t('event.messages.saveError'), 'error');
    return;
  }

  try {
    await eventStore.updateItem(eventData);
    notificationStore.showSnackbar(t('event.messages.updateSuccess'), 'success');
    closeForm();
  } catch (error) {
    notificationStore.showSnackbar(t('event.messages.saveError'), 'error');
  }
};

const closeForm = () => {
  router.push('/events');
};
</script>
